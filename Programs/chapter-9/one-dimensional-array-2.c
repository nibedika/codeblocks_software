#include<stdio.h>

int main()
{
    // ওয়ান ডাইমেনশনাল অ্যারে ডিক্লেয়ারেশন
    int num[25], i;

    // ওয়ান ডাইমেনশনাল অ্যারেতে মান ইনপুট নেওয়া
    for(i=0; i<5; i++)
    {
        scanf("%d", &num[i]);
    }

    // ওয়ান ডাইমেনশনাল অ্যারের মান প্রিন্ট সোজা ভাবে
    for(i=0; i<=4; i++)
    {
        printf("%d\t", num[i]);
    }
    printf("\n");

    // ওয়ান ডাইমেনশনাল অ্যারের মান প্রিন্ট বিপরীত ভাবে
    for(i=4; i>=0; i--)
    {
        printf("%d\t", num[i]);
    }

    return 0;
}
